package com.sist.r;

public class SeasonVO {
	
	private String season;
	private String food;
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	private int count;
	
	public String getSeason() {
		return season;
	}
	public void setSeason(String season) {
		this.season = season;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
